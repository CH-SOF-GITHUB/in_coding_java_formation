package Java;

public class Multiplication {

	public static void main(String[] args) {
		// Declaration des variables
		int a;
		int b, c;
		// Initialisation des variables
		a = 5;
		b = 7;
		// Calcul
		c = a * b;
		// Affichage
		System.out.println("Le résultat de la multiplication de " + a + " et " + b + " est égal à " + c);
	}
}
